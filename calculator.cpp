#include<iostream>
using namespace std;

int main(){
	int a, b;
	
	cout<<"Enter 1st number:\n";
	cin>>a;
	cout<<"Enter 2nd number:\n";
	cin>>b;
	
	cout << "a+b = " << a + b << endl;
	cout << "a-b = " << a - b << endl;
	cout << "a*b = " << a * b << endl;
	cout << "a/b = " << a / b << endl;
	cout << "a%b = " << a % b << endl;

return 0;
}
